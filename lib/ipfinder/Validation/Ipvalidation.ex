defmodule Ipfinder.Validation.Ipvalidation do
  @moduledoc """
  
  #  IPFinder elixir Client Library

  The official elixir client library for the [IPFinder.io](https://ipfinder.io) get details for :
  -  IP address details (city, region, country, postal code, latitude and more ..)
  -  ASN details (Organization name, registry,domain,comany_type, and more .. )
  -  Firewall by supported formats details (apache_allow,  nginx_deny, CIDR , and more ..)
  -  IP Address Ranges by the Organization name  details (list_asn, list_prefixes , and more ..)
  -  service status details (queriesPerDay, queriesLeft, key_type, key_info)
  - Get Domain IP (asn, organization,country_code ....)
  - Get Domain IP history (total_ip, list_ip,organization,asn ....)
  - Get list Domain By ASN, Country,Ranges (select_by , total_domain  , list_domain ....)
  - [GitHub ipfinder elixir](https://github.com/ipfinder-io/ip-finder-elixir)
  - [ipfinder](https://ipfinder.io/)

  ## Documentation for Ip validation.
  """
  @moduledoc since: "1.0.0"

  @doc """
   Helper method for validating an argument if it is (IPV4|IPV6) IP address

   ## Parameters
     * `ip` -  single IPv4 or IPv6 IP address
  """
  @doc since: "1.0.0"
  def validate(ip) do
    regex =
      ~r/^(([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])\.){3}([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])$|^(([a-zA-Z]|[a-zA-Z][a-zA-Z0-9\-]*[a-zA-Z0-9])\.)*([A-Za-z]|[A-Za-z][A-Za-z0-9\-]*[A-Za-z0-9])$|^\s*((([0-9A-Fa-f]{1,4}:){7}([0-9A-Fa-f]{1,4}|:))|(([0-9A-Fa-f]{1,4}:){6}(:[0-9A-Fa-f]{1,4}|((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){5}(((:[0-9A-Fa-f]{1,4}){1,2})|:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){4}(((:[0-9A-Fa-f]{1,4}){1,3})|((:[0-9A-Fa-f]{1,4})?:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){3}(((:[0-9A-Fa-f]{1,4}){1,4})|((:[0-9A-Fa-f]{1,4}){0,2}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){2}(((:[0-9A-Fa-f]{1,4}){1,5})|((:[0-9A-Fa-f]{1,4}){0,3}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){1}(((:[0-9A-Fa-f]{1,4}){1,6})|((:[0-9A-Fa-f]{1,4}){0,4}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(:(((:[0-9A-Fa-f]{1,4}){1,7})|((:[0-9A-Fa-f]{1,4}){0,5}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:)))(%.+)?\s*$/

    if String.match?(ip, regex) == false do
      raise "Invalid IPaddress"
    end
  end
end
